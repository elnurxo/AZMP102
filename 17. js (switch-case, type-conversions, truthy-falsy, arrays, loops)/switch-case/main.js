// switch case - condition statement
// if, else, else if

let weekDay = 7;

switch (weekDay) {
  case 1:
    console.log("monday");
    break;
  case 2:
    console.log("tuesday");
    break;
  case 3:
    console.log("wednesday");
    break;
  case 4:
    console.log("thursday");
    break;
  case 5:
    console.log("friday");
    break;
  case 6:
  case 7:
    console.log("weekend");
    break;

  default:
    console.log("invalid input");
    break;
}
